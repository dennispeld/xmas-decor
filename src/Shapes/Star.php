<?php

namespace App\Shapes;

class Star implements Pattern
{
    /**
     * Get the pattern of Star shape
     *
     * @param string $size
     * @return array
     */
    public function get($size): array
    {
        switch ($size) {
            case 'S':
                return [
                    1 => [[' ' => 3], ['+' => 1]],
                    2 => [[' ' => 3], ['X' => 1]],
                    3 => [['+' => 1], ['X' => 5], ['+' => 1]],
                    4 => [[' ' => 3], ['X' => 1]],
                    5 => [[' ' => 3], ['+' => 1]],
                ];
            case 'M':
                return [
                    1 => [[' ' => 5], ['+' => 1]],
                    2 => [[' ' => 5], ['X' => 1]],
                    3 => [[' ' => 3], ['X' => 5]],
                    4 => [['+' => 1], ['X' => 9], ['+' => 1]],
                    5 => [[' ' => 3], ['X' => 5]],
                    6 => [[' ' => 5], ['X' => 1]],
                    7 => [[' ' => 5], ['+' => 1]],
                ];
            case 'L':
                return [
                    1 => [[' ' => 8], ['+' => 1]],
                    2 => [[' ' => 8], ['X' => 1]],
                    3 => [[' ' => 7], ['X' => 3]],
                    4 => [[' ' => 5], ['X' => 7]],
                    5 => [[' ' => 3], ['X' => 11]],
                    6 => [['+' => 1], ['X' => 15], ['+' => 1]],
                    7 => [[' ' => 3], ['X' => 11]],
                    8 => [[' ' => 5], ['X' => 7]],
                    9 => [[' ' => 7], ['X' => 3]],
                    10 => [[' ' => 8], ['X' => 1]],
                    11 => [[' ' => 8], ['+' => 1]],
                ];
        }
    }
}