<?php

namespace App\Shapes;

class CloverPattern implements Pattern
{
    private string $size;

    public function __construct($size)
    {
        $this->size = $size;
    }

    /**
     * Get the pattern of Clover shape
     *
     * @return array
     */
    public function get(): array
    {
        switch ($this->size) {
            case 'S':
            case 'M':
            case 'L':
                return [
                    1 => [[' ' => 5], ['X' => 2], [' ' => 1], ['X' => 2], [' ' => 5]],
                    2 => [[' ' => 4], ['X' => 7], [' ' => 4]],
                    3 => [[' ' => 4], ['X' => 7], [' ' => 4]],
                    4 => [[' ' => 4], ['X' => 7], [' ' => 4]],
                    5 => [[' ' => 1], ['X' => 3], [' ' => 1], ['X' => 5], [' ' => 1], ['X' => 3], [' ' => 1]],
                    6 => [['X' => 5], [' ' => 1], ['X' => 3], [' ' => 1], ['X' => 5]],
                    7 => [['X' => 6], [' ' => 1], ['X' => 1], [' ' => 1], ['X' => 6]],
                    8 => [[' ' => 1], ['X' => 6], [' ' => 1], ['X' => 6], [' ' => 1]],
                    9 => [['X' => 6], [' ' => 1], ['X' => 1], [' ' => 1], ['X' => 6]],
                    10 => [['X' => 5], [' ' => 2], ['X' => 1], [' ' => 2], ['X' => 5]],
                    11 => [[' ' => 1], ['X' => 3], [' ' => 3], ['X' => 1], [' ' => 3], ['X' => 3], [' ' => 1]],
                    12 => [[' ' => 6], ['X' => 2], [' ' => 7]],
                    13 => [[' ' => 5], ['X' => 2], [' ' => 8]],
                    14 => [[' ' => 2], ['X' => 4], [' ' => 9]],
                    15 => [[' ' => 2], ['X' => 2], [' ' => 11]],
                ];
            default:
                return [];
        }
    }
}